package com.od.sharemarketmarathi;

public class MyBasic {
    String ti; //
    String tx;

    public MyBasic() {
    }

    public MyBasic(String ti, String tx) {
        this.ti = ti;
        this.tx = tx;
    }

    public String getTi() {
        return ti;
    }

    public void setTi(String ti) {
        this.ti = ti;
    }

    public String getTx() {
        return tx;
    }

    public void setTx(String tx) {
        this.tx = tx;
    }
}
